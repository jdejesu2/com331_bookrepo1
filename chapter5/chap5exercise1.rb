##conversions
##To get the string version of an object, we simply write .to_s after it:
var1 =  2
var2 = '5'
puts var1.to_s + var2
##OUTPUT
##25

##Similarly, .to_i gives the integer version of an object, and .to_f gives the float version.
var1 =  2
var2 = '5'
puts var1.to_s + var2
puts var1 + var2.to_i
##OUTPUT
##25
##7
